    if target_date is None:
        target_date = date.today()

    random.seed(target_date.toordinal())
    chosen = random.sample(RECIPES, 2)

    date_str = target_date.strftime("%Y-%m-%d")
    formatted = target_date.strftime("%d %B %Y")

    # Map month numbers to Hindi names
    hindi_months = {
        1: "जनवरी", 2: "फ़रवरी", 3: "मार्च", 4: "अप्रैल",
        5: "मई", 6: "जून", 7: "जुलाई", 8: "अगस्त",
        9: "सितंबर", 10: "अक्टूबर", 11: "नवंबर", 12: "दिसंबर",
    }
    hindi_date = f"{target_date.day} {hindi_months[target_date.month]} {target_date.year}"

    content = f"# आज की शाकाहारी रेसिपी — {hindi_date}\n\n---\n\n"
    content += format_recipe(chosen[0], 1)
    content += "\n\n---\n\n"
    content += format_recipe(chosen[1], 2)
    content += "\n"

    os.makedirs("recipes", exist_ok=True)
    filepath = os.path.join("recipes", f"{date_str}.md")
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"रेसिपी फ़ाइल बनाई गई: {filepath}")
    print(f"  1. {chosen[0]['name']}")
    print(f"  2. {chosen[1]['name']}")
    return filepath


