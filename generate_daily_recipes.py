HINDI_MONTHS = {
    1:"जनवरी", 2:"फ़रवरी", 3:"मार्च", 4:"अप्रैल",
    5:"मई", 6:"जून", 7:"जुलाई", 8:"अगस्त",
    9:"सितंबर", 10:"अक्टूबर", 11:"नवंबर", 12:"दिसंबर",
}


def format_recipe(recipe, number):
    lines = [
        f"## रेसिपी {number}: {recipe['name']}",
        "",
        f"**समय:** {recipe['time']} | **सर्विंग:** {recipe['servings']}",
        "",
        "### सामग्री (और विकल्प)",
    ]
    for name, qty, swaps in recipe["ingredients"]:
        swap_str = " / ".join(swaps)
        lines.append(f"- **{name}** — {qty}")
        lines.append(f"  - _विकल्प: {swap_str}_")
    lines += ["", "### विधि"]
    for i, step in enumerate(recipe["steps"], 1):
        lines.append(f"{i}. {step}")
    return "\n".join(lines)


def shopping_list(recipes):
    lines = ["### खरीदारी सूची (Amazon/WhatsApp के लिए)"]
    for r in recipes:
        lines.append(f"\n**{r['name']}:**")
        for name, qty, _ in r["ingredients"]:
            lines.append(f"- {name} — {qty}")
    return "\n".join(lines)


def generate_for_date(target_date=None):
    if target_date is None:
        target_date = date.today()

    random.seed(target_date.toordinal())
    chosen = random.sample(RECIPES, 2)

    date_str = target_date.strftime("%Y-%m-%d")
    hindi_date = f"{target_date.day} {HINDI_MONTHS[target_date.month]} {target_date.year}"

    content = f"# आज की शाकाहारी रेसिपी — {hindi_date}\n\n---\n\n"
    content += format_recipe(chosen[0], 1)
    content += "\n\n---\n\n"
    content += format_recipe(chosen[1], 2)
    content += "\n\n---\n\n"
    content += shopping_list(chosen)
    content += "\n"

    os.makedirs("recipes", exist_ok=True)
    filepath = os.path.join("recipes", f"{date_str}.md")
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"✓ रेसिपी फ़ाइल बनाई गई: {filepath}")
    print(f"  1. {chosen[0]['name']}")
    print(f"  2. {chosen[1]['name']}")

    # Print WhatsApp-ready shopping list
    print("\n📱 WhatsApp सूची:")
    print(f"🍛 *आज की रसोई — {hindi_date}*")
    for r in chosen:
        print(f"\n*{r['name']}:*")
        for name, qty, _ in r["ingredients"]:
            print(f"• {name} — {qty}")

    return filepath


if __name__ == "__main__":
    import sys
    if len(sys.argv) > 1:
        from datetime import datetime
        d = datetime.strptime(sys.argv[1], "%Y-%m-%d").date()
        generate_for_date(d)
    else:
        generate_for_date()
